// Get topics for a subject and class (with fallback to nearest class)
export function getTopics(subject, cls) {
  const s = SYLLABUS[subject]
  if (!s) return SYLLABUS.English[1]
  if (s[cls]) return s[cls]
  // Fall back to nearest available class
  const keys = Object.keys(s).map(Number).sort((a, b) => a - b)
  const best = keys.reduce((p, c) => (Math.abs(c - cls) < Math.abs(p - cls) ? c : p), keys[0])
  return s[best]
}

// Get effective class for subjects that only cover certain grades
export function getEffectiveClass(subject, cls) {
  const s = SYLLABUS[subject]
  if (!s) return cls
  const keys = Object.keys(s).map(Number)
  if (keys.includes(cls)) return cls
  return keys.reduce((p, c) => (Math.abs(c - cls) < Math.abs(p - cls) ? c : p), keys[0])
}
